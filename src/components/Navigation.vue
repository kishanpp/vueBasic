<script>

	global.jQuery = require('jquery')
	var $ = global.jQuery
	window.$ = $
	window.jQuery = global.jQuery
	
	// import auth from '@/api/authentication.js'
	
	export default {
		name : 'Navigation',
		components: {
			
		},
		data() {
            return {
				// not able to bind DOM elements since executed before DOM rendering
                responsiveNav : $('#responsive-nav'),
				// catToggle : $('#responsive-nav .category-nav .category-header'),
				catList : $('#responsive-nav .category-nav .category-list'),
				// menuToggle : $('#responsive-nav .menu-nav .menu-header'),
				menuList : $('#responsive-nav .menu-nav .menu-list'),
            }
        },
        validations: {
			
        },
		methods: {
			catToggle: function (){
				this.menuList.removeClass('open')
				this.catList.toggleClass('open')
			},
			menuToggle: function (){
				this.catList.removeClass('open');
				this.menuList.toggleClass('open');
			},
		},
		// called after data() is set, so make api calls on this
		created: function () {
		},
		// called after DOM is ready so do dom manipulations here.
		mounted: function () {
			let ref = this
			$(document).click(function(event) {
				if (!$(event.target).closest(ref.responsiveNav).length) {
					if (ref.responsiveNav.hasClass('open')) {
						ref.responsiveNav.removeClass('open')
						$('#navigation').removeClass('shadow')
					} else {
						if ($(event.target).closest('.nav-toggle > button').length) {
							if (!ref.menuList.hasClass('open') && !ref.catList.hasClass('open')) {
								ref.menuList.addClass('open')
							}
							$('#navigation').addClass('shadow')
							ref.responsiveNav.addClass('open')
						}
					}
				}
			})
			
			// assign dom elements after DOM rendering to bind.
			this.responsiveNav = $('#responsive-nav')
			// catToggle : $('#responsive-nav .category-nav .category-header'),
			this.catList = $('#responsive-nav .category-nav .category-list')
			// menuToggle : $('#responsive-nav .menu-nav .menu-header'),
			this.menuList = $('#responsive-nav .menu-nav .menu-list')
		},
		computed: {
			active: function (){
				return (this.$router.currentRoute.name === 'home') ? false : true				
			}
		},
		watch: {
			$route: function(){
			}
		}
	}
</script>